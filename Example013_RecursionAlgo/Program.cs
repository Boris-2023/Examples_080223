//Factorial by Recursion

double Factorial(int n)
{
    //1! == 1, 0! = 1
    if (n == 1) return 1;
    else return n * Factorial(n - 1);
}

Console.WriteLine(Factorial(20));//1*2*3 = 6



//Fibonacci row by Recursion: f(1)=1, f(2)=1,...,f(n) = f(n-1) + f(n-2);

double Fibo(int n)
{
    if(n==1 || n==2) return 1;
    else return Fibo(n-1) + Fibo(n-2);
}

